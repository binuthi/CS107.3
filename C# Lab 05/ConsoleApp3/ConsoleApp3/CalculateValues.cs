using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp3
{
    internal class CalculateValues
    {
        public int Addition(int n01,int n02)
        {
            return n01 + n02;
        }
        public int Substraction(int n01, int n02)
        {
            return n01 - n02;
        }
        public int Multiplication(int n01, int n02)
        {
            return n01 * n02;
        }
        public int Division(int n01, int n02)
        {
            return n01 / n02;
        }
    }
}
